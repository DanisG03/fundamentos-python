# Lista de productos
productos = ["Cuaderno", "Lapicero", "Borrador", "Regla", "Colores", "Mochila"]

# Lista de precios
precios = [3500.5, 2000.0, 1000.0, 2500.0, 12000.0, 45000.0]

# Lista de cantidades
cantidades = [20, 35, 15, 10, 8, 5]

# Imprimir listas
print("Productos:", productos)
print("Precios:", precios)
print("Cantidades:", cantidades)

# Cantidad de productos
print("Número de productos:", len(productos))

# Tipo de dato de la lista
print("Tipo de la lista productos:", type(productos))

# Tipo de dato del primer elemento
print("Tipo del primer elemento:", type(productos[0]))

# Diferencia:
# type(productos) muestra que es una lista.
# type(productos[0]) muestra el tipo de un elemento dentro de la lista.